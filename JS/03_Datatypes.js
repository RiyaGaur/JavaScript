// Primitive Datatypes

//String
let name="Riya";
console.log("Datatype is " + (typeof name));

// Numbers
let marks = 36;
console.log("Datatype is " + typeof marks);

// Boolean
let isDriver = true;
console.log("Datatype is " + typeof isDriver);

// Null
let nullvar = null;
console.log("Datatype is " + typeof nullvar);

// Undefined
let undef = undefined;
console.log("Datatype is " + typeof undef);

// Reference Datatypes

// Array
let arr = [1,2,3,"4"];
console.log("Datatype is " + typeof arr);

// Objects
let stdmarks = {
    A: 34,
    B: 36,
    C: 40
};
console.log("Datatype is " + typeof stdmarks);

